# part 1 calculator

class Calculator
	def self.add(a,b)
		a + b
	end

	def self.subtract(c,d)
		c - d
	end

	def self.multiply(e,f)
		e * f
	end

	def self.divide(g,h)
		g / h
	end
end

puts Calculator.add 10,15
puts Calculator.subtract 15,10
puts Calculator.multiply 5,2
puts Calculator.divide 10,2

